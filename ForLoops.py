# For Loops

x = 2
while x < 5:
    print(x)
    x = x + 1

# For Loop - A for loop is used to "iterate" over a number of variables
# or objects (or elements)



# This is to iterate through a list
fruits = ["apple","banana","mango","cherry"]
for x in fruits:
    print(x)



# This is to run code a certain number of times
for i in range(1,10):
    print(i)

# When you have "1 to 10" the code will never use the last number
